def AFE_modifyRegGlobal(param, value):
	
	size = param.msb + 1 - param.lsb
	a = np.zeros((1,8),dtype = 'uint32')
	
	a[0][0] = (0xffffff) # for 24 bit registers
	a[0][1] = (a[0][0]<<size) & a[0][0] 
	a[0][1] = a[0][1] ^ a[0][0] # --> writeMask
	
	a[0][2] = a[0][1] & int(value) # --> writeVal
	
	a[0][3] = AFE_readReg(param.address) # --> readVal
	
	a[0][4] = ((a[0][0]<<(param.msb+1))^ (a[0][0]<<param.lsb)) & a[0][0]
	a[0][5] = a[0][4] ^ a[0][0] # --> readMask
	a[0][6] = a[0][3] & a[0][5]
	
	a[0][7] = a[0][6] | ((a[0][2] << param.lsb) & a[0][0])
	
	AFE_writeReg(param.address,a[0][7])


def AFE_modifyRegPPM(PhNo,param,value):
	# i.e. switching between GLOBAL.HIGH_PRF and PPM.ILED_DRV1 modes

	# enabling PAGE_SEL bit
	AFE_modifyRegGlobal(afe.Page0.PAGE_SEL,     True)
	Absolute_address = (PhNo-1)*8 + param.address
	
	temp_param = AFE_RegMap_h.sigParameter()
	temp_param.address = Absolute_address
	temp_param.msb = param.msb
	temp_param.lsb = param.lsb

	AFE_modifyRegGlobal(temp_param,value)

	# disabling PAGE_SEL bit
	AFE_modifyRegGlobal(afe.Page0.PAGE_SEL,     False)


